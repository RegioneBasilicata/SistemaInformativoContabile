package it.latraccia.utility;

public class CreaContoBancarioResult 
{
	String idAnagrafica;
	String idSede;
	String idTipoPagamento;
	String idContoCorrente;
	int codiceRisposta = 1;
	String descrizioneRisposta;
	
	/**
	 * @return the idAnagrafica
	 */
	public String getIdAnagrafica() {
		return idAnagrafica;
	}
	/**
	 * @param idAnagrafica the idAnagrafica to set
	 */
	public void setIdAnagrafica(String idAnagrafica) {
		this.idAnagrafica = idAnagrafica;
	}
	/**
	 * @return the idSede
	 */
	public String getIdSede() {
		return idSede;
	}
	/**
	 * @param idSede the idSede to set
	 */
	public void setIdSede(String idSede) {
		this.idSede = idSede;
	}
	/**
	 * @return the idTipoPagamento
	 */
	public String getIdTipoPagamento() {
		return idTipoPagamento;
	}
	/**
	 * @param idTipoPagamento the idTipoPagamento to set
	 */
	public void setIdTipoPagamento(String idTipoPagamento) {
		this.idTipoPagamento = idTipoPagamento;
	}
	/**
	 * @return the idContoCorrente
	 */
	public String getIdContoCorrente() {
		return idContoCorrente;
	}
	/**
	 * @param idContoCorrente the idContoCorrente to set
	 */
	public void setIdContoCorrente(String idContoCorrente) {
		this.idContoCorrente = idContoCorrente;
	}
	/**
	 * @return the codiceRisposta
	 */
	public int getCodiceRisposta() {
		return codiceRisposta;
	}
	/**
	 * @param codiceRisposta the codiceRisposta to set
	 */
	public void setCodiceRisposta(int codiceRisposta) {
		this.codiceRisposta = codiceRisposta;
	}
	/**
	 * @return the descrizioneRisposta
	 */
	public String getDescrizioneRisposta() {
		return descrizioneRisposta;
	}
	/**
	 * @param descrizioneRisposta the descrizioneRisposta to set
	 */
	public void setDescrizioneRisposta(String descrizioneRisposta) {
		this.descrizioneRisposta = descrizioneRisposta;
	}
}
