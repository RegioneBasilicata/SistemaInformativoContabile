package it.latraccia.utility;

public class InterrogaBeneficiariLiqResult 
{
	private String idBeneficiario;
	private String idSede;
	private double importoOriginario;
	private double importoResiduo;
	
	/**
	 * @return the idBeneficiario
	 */
	public String getIdBeneficiario() {
		return idBeneficiario;
	}
	/**
	 * @param idBeneficiario the idBeneficiario to set
	 */
	public void setIdBeneficiario(String idBeneficiario) {
		this.idBeneficiario = idBeneficiario;
	}
	/**
	 * @return the idSede
	 */
	public String getIdSede() {
		return idSede;
	}
	/**
	 * @param idSede the idSede to set
	 */
	public void setIdSede(String idSede) {
		this.idSede = idSede;
	}
	/**
	 * @return the importoOriginario
	 */
	public double getImportoOriginario() {
		return importoOriginario;
	}
	/**
	 * @param importoOriginario the importoOriginario to set
	 */
	public void setImportoOriginario(double importoOriginario) {
		this.importoOriginario = importoOriginario;
	}
	/**
	 * @return the importoResiduo
	 */
	public double getImportoResiduo() {
		return importoResiduo;
	}
	/**
	 * @param importoResiduo the importoResiduo to set
	 */
	public void setImportoResiduo(double importoResiduo) {
		this.importoResiduo = importoResiduo;
	}
	
}
